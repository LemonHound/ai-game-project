    evaluatePosition(board, gameStatus = {}) {
        if (gameStatus.gameOver) {
            if (gameStatus.winner === 'white') {
                return { type: 'mate', value: 100, depth: 0 };
            } else if (gameStatus.winner === 'black') {
                return { type: 'mate', value: -100, depth: 0 };
            } else {
                return { type: 'cp', value: 0, depth: 0 };
            }
        }

        let evaluation = 0;

        for (let row = 0; row < 8; row++) {
            for (let col = 0; col < 8; col++) {
                const piece = board[row][col];
                if (!piece) continue;

                const isWhite = piece === piece.toUpperCase();
                const pieceType = piece.toLowerCase();
                const pieceValue = this.pieceValues[piece];

                let positionBonus = 0;
                if (this.positionBonuses[pieceType]) {
                    const bonusRow = isWhite ? row : 7 - row;
                    positionBonus = this.positionBonuses[pieceType][bonusRow][col];
                }

                const totalValue = pieceValue + positionBonus;
                evaluation += isWhite ? totalValue : -totalValue;
            }
        }

        return {
            type: 'cp',
            value: evaluation,
            depth: 1,
        };
    }

    async analyze(board, gameStatus, depth = 1) {
        return new Promise(resolve => {
            setTimeout(() => {
                const evaluation = this.evaluatePosition(board, gameStatus);
                resolve(evaluation);
            }, 100);
        });
    }
